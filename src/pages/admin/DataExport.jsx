/* ── helpers ─────────────────────────────────────────────────── */
const fmt = (d) => { try { return format(parseISO(d), 'd MMM yyyy') } catch { return d ?? '—' } }
const fmtTime = (d) => { try { return format(parseISO(d), 'h:mm a') } catch { return '—' } }
const fmtDt = (d) => { try { return format(parseISO(d), 'd MMM yyyy, h:mm a') } catch { return d ?? '—' } }
const iso = (d) => format(d, 'yyyy-MM-dd')

const today = () => iso(new Date())
const firstOfMonth = () => iso(startOfMonth(new Date()))

/* ── CSV builder ─────────────────────────────────────────────── */
function buildCSV(headers, rows) {
  const escape = (v) => {
    const s = String(v ?? '')
    return s.includes(',') || s.includes('"') || s.includes('\n')
      ? `"${s.replace(/"/g, '""')}"` : s
  }
  const lines = [headers.map(escape).join(',')]
  for (const row of rows) lines.push(row.map(escape).join(','))
  return lines.join('\r\n')
}

function downloadBlob(content, filename, mime) {
  const blob = new Blob([content], { type: mime })
  const url  = URL.createObjectURL(blob)
  const a    = document.createElement('a')
  a.href = url
  a.download = filename
  a.click()
  URL.revokeObjectURL(url)
}

/* ── PDF helpers ─────────────────────────────────────────────── */
async function generatePDF({ title, period, adminName, adminId, headers, rows, filename }) {
  // dynamic import so main bundle stays lean
  const { default: jsPDF } = await import('jspdf')
  const { default: autoTable } = await import('jspdf-autotable')

  const doc = new jsPDF({ orientation: 'landscape', unit: 'mm', format: 'a4' })
  const pageW = doc.internal.pageSize.getWidth()

  // Header block
  doc.setFontSize(20)
  doc.setTextColor(0, 174, 239)
  doc.setFont('helvetica', 'bold')
  doc.text('FUEL TRACKS', 14, 18)

  doc.setFontSize(9)
  doc.setTextColor(107, 114, 128)
  doc.setFont('helvetica', 'normal')
  doc.text('Employee Attendance Portal', 14, 24)

  doc.setFontSize(13)
  doc.setTextColor(27, 58, 107)
  doc.setFont('helvetica', 'bold')
  doc.text(title, 14, 33)

  doc.setFontSize(8)
  doc.setFont('helvetica', 'normal')
  doc.setTextColor(107, 114, 128)
  if (period) doc.text(`Period: ${period}`, 14, 39)
  doc.text(`Generated on: ${format(new Date(), 'd MMM yyyy, h:mm a')}`, 14, period ? 44 : 39)

  // Divider
  doc.setDrawColor(219, 234, 254)
  doc.setLineWidth(0.5)
  doc.line(14, period ? 48 : 44, pageW - 14, period ? 48 : 44)

  // Table
  autoTable(doc, {
    startY: period ? 52 : 48,
    head: [headers],
    body: rows,
    styles: { fontSize: 7.5, cellPadding: 2.5, font: 'helvetica' },
    headStyles: {
      fillColor: [27, 58, 107],
      textColor: [255, 255, 255],
      fontStyle: 'bold',
    },
    alternateRowStyles: { fillColor: [240, 247, 255] },
    didDrawPage: (data) => {
      const pageCount = doc.internal.getNumberOfPages()
      const currentPage = data.pageNumber
      doc.setFontSize(7)
      doc.setTextColor(107, 114, 128)
      const footerY = doc.internal.pageSize.getHeight() - 7
      doc.text('Fuel Tracks — Confidential', 14, footerY)
      doc.text(`Page ${currentPage} of ${pageCount}`, pageW / 2, footerY, { align: 'center' })
      doc.text(`Generated by: ${adminName} (${adminId})`, pageW - 14, footerY, { align: 'right' })
    },
  })

  doc.save(filename)
}

