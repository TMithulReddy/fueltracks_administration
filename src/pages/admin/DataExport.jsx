  async function generateAttendanceReport({ dateSlug }) {
    // Fetch data
    let query = supabase
      .from('login_history')
      .select(`
        id, login_at, logout_at, hours_worked, session_duration_minutes, status, event_type,
        profile:profiles!profile_id(employee_id, full_name, is_active, role,
          details:employee_details!profile_id(department))
      `)
      .eq('status', 'success')
      .eq('event_type', 'login')
      .eq('profile.role', 'employee')
      .gte('login_at', `${fromDate}T00:00:00.000Z`)
      .lte('login_at', `${toDate}T23:59:59.999Z`)
      .order('login_at', { ascending: true })

    if (employeeFilter === 'specific' && selectedEmployee) {
      query = query.eq('profile_id', selectedEmployee.id)
    }

    const { data, error } = await query
    if (error) throw error

    const rows = (data ?? []).filter(r => r.profile?.role === 'employee')

    const headers = ['Employee ID', 'Employee Name', 'Department', 'Date', 'Login Time', 'Logout Time', 'Hours Worked', 'Session Duration (mins)']

    const tableRows = rows.map(r => {
      const profileDetails = (Array.isArray(r.profile?.details) ? r.profile.details[0] : r.profile?.details) || {}
      
      let logoutTimeLabel = 'Active Session'
      let hrsWorkedLabel = '—'
      let durationLabel = '—'

      if (r.logout_at) {
        logoutTimeLabel = fmtTime(r.logout_at)
        hrsWorkedLabel = r.hours_worked != null ? parseFloat(r.hours_worked).toFixed(2) : '—'
        durationLabel = r.session_duration_minutes ?? '—'
      } else {
        const loginTime = new Date(r.login_at).getTime()
        const nowTime = new Date().getTime()
        const diffMs = nowTime - loginTime
        const liveMins = Math.max(0, Math.round(diffMs / (1000 * 60)))
        const liveHrs = parseFloat((liveMins / 60).toFixed(2))

        logoutTimeLabel = 'Active Session (Online)'
        hrsWorkedLabel = `${liveHrs.toFixed(2)} (live)`
        durationLabel = `${liveMins} (live)`
      }

      return [
        r.profile?.employee_id ?? '—',
        r.profile?.full_name ?? '—',
        profileDetails.department ?? '—',
        fmt(r.login_at),
        fmtTime(r.login_at),
        logoutTimeLabel,
        hrsWorkedLabel,
        durationLabel,
      ]
    })

    const periodStr = `${fmt(fromDate + 'T00:00:00')} – ${fmt(toDate + 'T00:00:00')}`

    if (fileFormat === 'csv') {
      const csv = buildCSV(headers, tableRows)
      downloadBlob(csv, `fueltracks-attendance-${dateSlug}.csv`, 'text/csv;charset=utf-8;')
    } else {
      await generatePDF({
        title: 'Attendance Report',
        period: periodStr,
        adminName: currentAdminProfile?.full_name ?? 'Admin',
        adminId: currentAdminProfile?.employee_id ?? '—',
        headers,
        rows: tableRows,
        filename: `fueltracks-attendance-${dateSlug}.pdf`,
      })
    }
  }

  async function generateSummaryReport({ dateSlug }) {
    let query = supabase
      .from('profiles')
      .select(`
        id, employee_id, full_name, email, phone, role, is_active,
        details:employee_details!profile_id(department, designation, employment_type, date_of_joining, total_working_days, total_working_hours)
      `)
      .eq('role', 'employee')
      .eq('is_active', true)
      .order('employee_id', { ascending: true })

    if (employeeFilter === 'specific' && selectedEmployee) {
      query = query.eq('id', selectedEmployee.id)
    }

    const { data, error } = await query
    if (error) throw error

    // Fetch successful login history records to calculate analytics dynamically
    const { data: allHistory, error: histError } = await supabase
      .from('login_history')
      .select('profile_id, login_at, logout_at, hours_worked')
      .eq('status', 'success')

    if (histError) throw histError

    // Group successful logs by profile_id
    const historyMap = {}
    if (allHistory) {
      for (const item of allHistory) {
        const pid = item.profile_id
        if (!historyMap[pid]) {
          historyMap[pid] = { days: new Set(), hours: 0 }
        }
        if (item.login_at) {
          historyMap[pid].days.add(item.login_at.split('T')[0])
        }
        if (item.logout_at) {
          historyMap[pid].hours += parseFloat(item.hours_worked ?? 0)
        } else {
          // Running active session: live hours worked
          const loginTime = new Date(item.login_at).getTime()
          const nowTime = new Date().getTime()
          const diffMs = nowTime - loginTime
          const diffMins = Math.max(0, Math.round(diffMs / (1000 * 60)))
          const liveHrs = parseFloat((diffMins / 60).toFixed(2))
          historyMap[pid].hours += liveHrs
        }
      }
    }

    const headers = ['Employee ID', 'Full Name', 'Email', 'Phone', 'Department', 'Designation', 'Employment Type', 'Date of Joining', 'Total Working Days', 'Total Working Hours']

    const tableRows = (data ?? []).map(r => {
      const d = (Array.isArray(r.details) ? r.details[0] : r.details) || {}
      const stats = historyMap[r.id] || { days: new Set(), hours: 0 }
      return [
        r.employee_id ?? '—',
        r.full_name ?? '—',
        r.email ?? '—',
        r.phone ?? '—',
        d.department ?? '—',
        d.designation ?? '—',
        d.employment_type ?? '—',
        d.date_of_joining ? fmt(d.date_of_joining + 'T00:00:00') : '—',
        stats.days.size,
        stats.hours.toFixed(2),
      ]
    })

    if (fileFormat === 'csv') {
      const csv = buildCSV(headers, tableRows)
      downloadBlob(csv, `fueltracks-employee-summary-${dateSlug}.csv`, 'text/csv;charset=utf-8;')
    } else {
      await generatePDF({
        title: 'Employee Summary',
        period: null,
        adminName: currentAdminProfile?.full_name ?? 'Admin',
        adminId: currentAdminProfile?.employee_id ?? '—',
        headers,
        rows: tableRows,
        filename: `fueltracks-employee-summary-${dateSlug}.pdf`,
      })
    }
  }

